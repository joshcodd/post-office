    public function apiStore(Request $request)
    {
        $comment = new Comment();
        $comment->post_id = $request['post_id'];
        $comment->user_id = $request['user_id'];
        $comment->content = $request['content'];
        $comment->save();
        return $comment->load('user');
    }

